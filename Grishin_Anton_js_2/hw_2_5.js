/* Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return. */

function sum(x, y) {
    return x + y
}

function sub(x, y) {
    return x - y
}

function mul(x, y) {
    return x * y
}

function div(x, y) {
    return x / y
}

var a = 4
var b = 2
sum(a, b) // 6
sub(a, b) // 2
mul(a, b) // 8
div(a, b) // 2